using System;

class RandomLifeAdviceGenerator
{
    static void Main()
    {
        string[] advices =
        {
            "Винаги бъди себе си.",
            "Не се страхувай да правиш грешки – така се учиш.",
            "Здравето е най-голямото богатство.",
            "Цени времето си – то е най-ценният ресурс.",
            "Не сравнявай живота си с този на другите.",
            "Научи се да казваш 'не'.",
            "Малките неща правят живота красив.",
            "Бъди благодарен за това, което имаш.",
            "Всеки ден прави поне едно добро дело.",
            "Не отлагай за утре това, което можеш да направиш днес.",
            "Обръщай внимание на хората, които винаги са до теб.",
            "Животът е твърде кратък, за да се тревожиш за дреболии.",
            "Прощавай, но не забравяй уроците, които си научил.",
            "Научи се да се радваш на малките моменти.",
            "Успехът не се измерва само с пари, а и с щастие.",
            "Не се страхувай да напускаш комфортната си зона.",
            "Прави повече от това, което те кара да се чувстваш жив."
        };

        Random random = new Random();
        string userInput;

        Console.WriteLine("Добре дошли в програмата за случайни житейски съвети!");

        do
        {
            int index = random.Next(advices.Length);
            Console.WriteLine("\n Съвет: " + advices[index]);

            Console.Write("\nНатиснете 'Enter' за нов съвет или въведете 'exit' за изход: ");
            userInput = Console.ReadLine()?.Trim().ToLower();

        } while (userInput != "exit");

        Console.WriteLine("Благодаря, че използвахте програмата! ");
    }
}
